const NOTE_TITLES = {
  fireplace: 'Fireplace',
  surface: 'Surface',
  cord_concealment: 'Cord Concealment',
  soundbar: 'Soundbar',
  unmount: 'Unmount',
  misc: 'Misc',
};

function normalizeLabel(label) {
  return String(label || '')
    .replace(/\s+/g, ' ')
    .replace(/\.$/, '')
    .trim();
}

function isNoSelection(label) {
  const lower = normalizeLabel(label).toLowerCase();
  return (
    lower === 'no' ||
    lower === 'none' ||
    lower === 'other' ||
    lower.startsWith('no ') ||
    lower.startsWith('no -') ||
    lower.startsWith('no, ') ||
    lower.startsWith('no thanks') ||
    lower.startsWith('do not need') ||
    lower.includes('not needed') ||
    lower.includes('no sound bar') ||
    lower.includes('no soundbar') ||
    lower.includes('no unmount') ||
    lower.includes('do not unmount')
  );
}

function fieldCategory(fieldName) {
  const lower = normalizeLabel(fieldName).toLowerCase();
  if (lower.includes('size')) return 'size';
  if (lower.includes('fireplace')) return 'fireplace';
  if (lower.includes('sound bar') || lower.includes('soundbar')) return 'soundbar';
  if (lower.includes('conceal') || lower.includes('cord') || lower.includes('outlet') || lower.includes('conduit')) return 'cord_concealment';
  if (lower.includes('unmount')) return 'unmount';
  if (lower.includes('surface') || lower.includes('wall')) return 'surface';
  if (lower.includes('bracket') || lower.includes('mount')) return 'bracket';
  return 'misc';
}

function expandedSelections(fieldSelections, category, { includeNoSelections = false } = {}) {
  const out = [];
  for (const field of fieldSelections || []) {
    if (fieldCategory(field.fieldName) !== category) continue;
    for (const selection of field.selections || []) {
      const label = normalizeLabel(selection.label);
      if (!label) continue;
      if (!includeNoSelections && isNoSelection(label)) continue;
      const quantity = Math.max(1, Number(selection.quantity) || 1);
      for (let index = 0; index < quantity; index += 1) {
        out.push(label);
      }
    }
  }
  return out;
}

function expandedOptionSelections(optionSelections, predicate) {
  const out = [];
  for (const selection of optionSelections || []) {
    const label = normalizeLabel(selection.label);
    if (!label || isNoSelection(label)) continue;
    if (!predicate(label, selection)) continue;
    const quantity = Math.max(1, Number(selection.quantity) || 1);
    for (let index = 0; index < quantity; index += 1) {
      out.push(label);
    }
  }
  return out;
}

function summarize(labels) {
  const counts = new Map();
  for (const label of labels || []) {
    counts.set(label, (counts.get(label) || 0) + 1);
  }
  return [...counts.entries()].map(([label, count]) => (
    count > 1 ? `${label} x${count}` : label
  ));
}

function isAffirmativeFireplace(label) {
  const lower = normalizeLabel(label).toLowerCase();
  const isNegative = (
    lower.includes('not going above a fireplace') ||
    lower.includes('not above a fireplace') ||
    lower.includes('not over a fireplace') ||
    lower.includes('no fireplace') ||
    lower.includes('standard wall placement')
  );
  if (isNegative) return false;
  return (
    lower.includes('above a fireplace') ||
    lower.includes('above fireplace') ||
    lower.includes('over a fireplace') ||
    lower.includes('over fireplace')
  );
}

function selectBaseServiceMap(serviceName, optionSelections, fieldSelections) {
  if (!FIREPLACE_TV_SERVICE_MAP[serviceName]) {
    return {
      map: BASE_SERVICE_MAP[serviceName],
      fireplaceSelections: [],
      ambiguousFireplace: false,
      labelPrefix: serviceName || 'Service',
    };
  }

  const standardMap = TV_SERVICE_MAP[serviceName];
  const fireplaceMap = FIREPLACE_TV_SERVICE_MAP[serviceName];
  const sizes = collectServiceSelections(standardMap, optionSelections);
  const tvCount = sizes.length > 0 ? sizes.length : 1;
  const fireplaceSelections = expandedSelections(fieldSelections, 'fireplace', { includeNoSelections: true });
  const hasAffirmative = fireplaceSelections.some(isAffirmativeFireplace);
  const ambiguousFireplace = tvCount > 1 && hasAffirmative && fireplaceSelections.length !== tvCount;

  return {
    map: standardMap,
    fireplaceMap,
    fireplaceSelections,
    ambiguousFireplace,
    labelPrefix: 'TV Install',
  };
}

function collectServiceSelections(serviceMap, optionSelections) {
  if (!serviceMap) return [];
  return expandedOptionSelections(optionSelections, (label) => Boolean(serviceMap[label]));
}

function addBaseSegments(model, serviceName, optionSelections, fieldSelections) {
  const {
    map,
    fireplaceMap,
    fireplaceSelections,
    ambiguousFireplace,
    labelPrefix,
  } = selectBaseServiceMap(serviceName, optionSelections, fieldSelections);

  if (!map) {
    if (serviceName) {
      model.warnings.push('unknown_service');
      model.unknownOptions.push(serviceName);
      model.noteLines.push(`Unknown service: ${serviceName}`);
    }
    return [];
  }

  const selectedLabels = collectServiceSelections(map, optionSelections);
  const baseLabels = selectedLabels.length > 0 ? selectedLabels : ['_default'];
  const singleTvFireplace = fireplaceMap && baseLabels.length === 1 && fireplaceSelections.some(isAffirmativeFireplace);
  const fireplaceByIndex = fireplaceMap && baseLabels.length > 1 && fireplaceSelections.length === baseLabels.length
    ? fireplaceSelections.map(isAffirmativeFireplace)
    : null;

  if (ambiguousFireplace) {
    model.warnings.push('ambiguous_fireplace_selection');
    model.noteLines.push(`Ambiguous fireplace selection: ${summarize(fireplaceSelections.filter(isAffirmativeFireplace)).join('; ')}`);
  }

  baseLabels.forEach((label, index) => {
    const useFireplace = singleTvFireplace || fireplaceByIndex?.[index] === true;
    const effectiveMap = useFireplace ? fireplaceMap : map;
    const catalogObjectId = effectiveMap?.[label] || effectiveMap?._default;
    if (!catalogObjectId) return;
    const readableLabel = label === '_default' ? 'Default' : label;
    model.segmentItems.push({
      catalog_object_id: catalogObjectId,
      quantity: '1',
      label: useFireplace ? `${labelPrefix} over fireplace: ${readableLabel}` : `${labelPrefix}: ${readableLabel}`,
      segmentType: 'base_service',
    });
  });

  return baseLabels;
}

function addBracketSegments(model, optionSelections) {
  const bracketLabels = expandedOptionSelections(optionSelections, (label) => Boolean(TV_BRACKET_MAP[label]));
  for (const label of bracketLabels) {
    model.segmentItems.push({
      catalog_object_id: TV_BRACKET_MAP[label],
      quantity: '1',
      label: `Bracket: ${label}`,
      segmentType: 'bracket',
    });
  }
  return bracketLabels;
}

function addNoteSelections(model, fieldSelections, categories) {
  for (const category of categories) {
    const labels = expandedSelections(fieldSelections, category);
    if (labels.length === 0) continue;
    model.noteLines.push(`${NOTE_TITLES[category]}: ${summarize(labels).join('; ')}`);
  }
}

export function getMappedCatalogIds() {
  const ids = new Set();
  for (const serviceMap of [BASE_SERVICE_MAP, FIREPLACE_TV_SERVICE_MAP]) {
    for (const variations of Object.values(serviceMap)) {
      for (const id of Object.values(variations)) {
        if (id) ids.add(id);
      }
    }
  }
  for (const id of Object.values(TV_BRACKET_MAP)) {
    if (id) ids.add(id);
  }
  return [...ids];
}

export function buildSquareAppointmentModel({
  serviceName,
  fieldSelections = [],
  optionSelections = [],
  rawNotes = '',
} = {}) {
  const model = {
    segmentItems: [],
    noteLines: [],
    warnings: [],
    unknownOptions: [],
  };

  if (rawNotes) {
    model.noteLines.push(`ZenBooker notes: ${rawNotes}`);
  }

  const baseLabels = addBaseSegments(model, serviceName, optionSelections, fieldSelections);
  const bracketLabels = addBracketSegments(model, optionSelections);
  const noteBracketLabels = expandedSelections(fieldSelections, 'bracket');

  if (baseLabels.length > 0) {
    const readableBaseLabels = baseLabels.filter((label) => label !== '_default');
    model.noteLines.push(`TV sizes: ${summarize(readableBaseLabels).join('; ') || 'Default service variation'}`);
  }
  if (noteBracketLabels.length > 0 || bracketLabels.length > 0) {
    model.noteLines.push(`Brackets: ${summarize(noteBracketLabels.length > 0 ? noteBracketLabels : bracketLabels).join('; ')}`);
  }

  addNoteSelections(model, fieldSelections, ['surface', 'cord_concealment', 'soundbar', 'unmount', 'misc']);

  const unknownOptionLabels = expandedOptionSelections(optionSelections, (label, selection) => {
    const category = fieldCategory(selection.fieldName);
    if (category === 'bracket') return !TV_BRACKET_MAP[label];
    return category === 'misc' && !TV_BRACKET_MAP[label] && !BASE_SERVICE_MAP[serviceName]?.[label];
  });
  if (unknownOptionLabels.length > 0) {
    model.warnings.push('unknown_options');
    model.unknownOptions.push(...unknownOptionLabels);
    model.noteLines.push(`Unmapped ZenBooker options: ${summarize(unknownOptionLabels).join('; ')}`);
  }

  return {
    segmentItems: model.segmentItems,
    note: model.noteLines.join('\n').trim(),
    warnings: [...new Set(model.warnings)],
    unknownOptions: summarize(model.unknownOptions),
  };
}
